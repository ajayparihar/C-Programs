#include <stdio.h>

int main()
{
    printf("\n");
    printf("Char is %d Bytes\n\n", sizeof(char));
    printf("Short Int is %d Bytes\n\n", sizeof(short int));
    printf("Long Int is %d Bytes\n\n", sizeof(long int));
    printf("Float is %d Bytes\n\n", sizeof(float));
    printf("Double is %d Bytes\n\n", sizeof(double));
    printf("Long Double is %d Bytes\n\n", sizeof(long double));

    return 0;
}